export type TranslationKey = keyof typeof en;
const dictionaries = { en, vi };
type LanguageContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  toggleLanguage: () => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
  nodeText: (type: keyof typeof nodeTranslations.en) => {
    title: string;
    description: string;
    help: string;
  };
};
const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = window.localStorage.getItem('flow-bot-language');
    return saved === 'en' ? 'en' : 'vi';
  });

  useEffect(() => {
    window.localStorage.setItem('flow-bot-language', language);
    document.documentElement.lang = language;
  }, [language]);

  const t = useCallback(
    (key: TranslationKey, values?: Record<string, string | number>) => {
      let text: string = dictionaries[language][key];
      Object.entries(values || {}).forEach(([name, value]) => {
        text = text.split(`{{${name}}}`).join(String(value));
      });
      return text;
    },
    [language],
  );
  const nodeText = useCallback(
    (type: keyof typeof nodeTranslations.en) => {
      const [title, description, help] = nodeTranslations[language][type];
      return { title, description, help };
    },
    [language],
  );
  const toggleLanguage = useCallback(
    () => setLanguage((current) => (current === 'vi' ? 'en' : 'vi')),
    [],
  );
  const value = useMemo(
    () => ({ language, setLanguage, toggleLanguage, t, nodeText }),
    [language, nodeText, t, toggleLanguage],
  );
  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) throw new Error('useLanguage must be used inside LanguageProvider');
  return context;
}

