export function getLocalizedAuthError(message: string, t: (key: TranslationKey) => string) {
  const messages: Record<string, TranslationKey> = {
    'Email hoặc mật khẩu không đúng.': 'invalidCredentials',
    'Email không hợp lệ hoặc mật khẩu chưa đủ 8 ký tự.': 'invalidRegistration',
    'Email này đã được đăng ký.': 'emailRegistered',
    'Google Sign-In chưa được cấu hình.': 'googleNotConfigured',
    'Tài khoản Google không hợp lệ.': 'invalidGoogleAccount',
    'Không thể kết nối máy chủ.': 'serverUnavailable',
  };
  return messages[message] ? t(messages[message]) : message;
}
